import { useEffect, useState, useRef } from "react";
import { useMovies } from "./useMovies";
import { useLocalStorageState } from "./useLocalStorageState";
import StarRating from "./StarRating";
import { useKey } from "./useKey";

const average = (arr) =>
  arr.reduce((acc, cur, i, arr) => acc + cur / arr.length, 0);

const KEY = "d262c06";

export default function App() {
  const [query, setQuery] = useState("");
  const [selectedId, setSelectedId] = useState(null);

  // custom hook to get something from local storage
  const [watched, setWatched] = useLocalStorageState([], "watched"); // using a custom hook

  // BAD PRACTICE - Creates an infinite loop
  // fetch(`http://www.omdbapi.com/?apikey=${KEY}&s=race`)
  //   .then((res) => res.json())
  //   .then((data) => setMovies(data.Search))

  /* useEffect(function () {
    console.log("After initial render");
  }, []);

  useEffect(function () {
    console.log("After every render");
  });

  console.log("During render"); */

  const handleSelectMovie = function (id) {
    setSelectedId(id === selectedId ? null : id); // This closes the movie details if we click on the same movie twice
  };

  const handleCloseMovie = function () {
    setSelectedId(null);
  };

  const handleAddWatched = function (movie) {
    setWatched((watched) => [...watched, movie]);
  };

  const handleDeleteWatched = function (id) {
    setWatched((watched) => watched.filter((movie) => movie.imdbID !== id));
  };

  const { movies, isLoading, error } = useMovies(query);

  return (
    <>
      <NavBar>
        <Search query={query} setQuery={setQuery} />
        <NumResults movies={movies} />
      </NavBar>

      <Main>
        <Box>
          {/* {isLoading ? <Loader /> : <MovieList movies={movies} />} */}
          {isLoading && <Loader />}
          {!isLoading && !error && (
            <MovieList movies={movies} onSelectMovie={handleSelectMovie} />
          )}
          {error && <ErrorMessage message={error} />}
        </Box>
        <Box>
          {selectedId ? (
            <MovieDetails
              selectedId={selectedId}
              onCloseMovie={handleCloseMovie}
              onAddWatched={handleAddWatched}
              watched={watched}
            />
          ) : (
            <>
              <WatchedSummary watched={watched} />
              <WatchedMoviesList
                watched={watched}
                onDeleteWatched={handleDeleteWatched}
              />
            </>
          )}
        </Box>
      </Main>
    </>
  );
}

